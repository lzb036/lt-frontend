export function searchOperatorManualSections(keyword: string) {
  const normalizedKeyword = String(keyword || '').trim().toLowerCase()
  if (!normalizedKeyword) {
    return operatorManualSections
  }
  return operatorManualSections.filter((section) => {
    const searchable = [
      section.title,
      section.summary,
      section.route,
      section.permission,
      ...section.keywords,
      ...section.steps,
      ...section.checks,
      ...section.warnings,
    ].join('\n').toLowerCase()
    return searchable.includes(normalizedKeyword)
  })
}
